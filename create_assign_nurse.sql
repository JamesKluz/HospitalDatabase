CREATE TABLE Assign_nurse
(
  Patient_ID NUMBER(9) NOT NULL,
  Nurse_Id NUMBER(9) NOT NULL,
  PRIMARY KEY (Patient_ID, Nurse_Id),
  CONSTRAINT pid_assign_n_fk 
      FOREIGN KEY (Patient_ID) 
      REFERENCES Patient(Patient_ID)
      ON DELETE CASCADE,
  CONSTRAINT nid_assign_n_fk 
      FOREIGN KEY (Nurse_Id) 
      REFERENCES Nurse(Nurse_Id)
      ON DELETE CASCADE
);

